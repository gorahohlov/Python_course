a = int(input('Введите целое количество секунд в диапазоне от 0 до 86400: '))
hh = a // 3600
mm = (a - hh * 3600) // 60
ss = (a - hh * 3600 - mm * 60)
print('{:02}:{:02}:{:02}'.format(hh, mm, ss))
print('{0:02}:{1:02}:{2:02}'.format(hh, mm, ss))
print(f'{hh:02}:{mm:02}:{ss:02}')
